    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Game $game)
    {

        $round = Round::where('game_id', $game->id)->whereNull('finished_at')->with('tracks')->first();

        if(!$round) {

            $round = new Round([
                'game_id' => $game->id
            ]);
            $round->save();

            $tracks = Track::where('game_id', $game->id)->inRandomOrder()->limit($game->tracks_number)->get();

            foreach ($tracks as $track) {
               $round->tracks()->attach($track);
            }

        }

        return response()->json($round);
        
    }

